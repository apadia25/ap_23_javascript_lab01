//Given a program statement console.log(2 ^ 4);, check the output and explain why it is that way.

console.log(2 ^ 4);

//bitwise XOR
//In binary, 2 is represented as 10 and 4 is represented as 100.
//Performing a bitwise XOR operation on these binary representations, we get 110.
//Converting 110 back to decimal gives us 6.
//So, console.log(2 ^ 4); evaluates to 6 because it performs a bitwise XOR operation on 2 and 4, resulting in 6.